a = 10
b = 3.14
c = "Hello"
d = [1, 2, 3]
e = {"name": "Alice", "age": 30}

# Выводим значения
print("a =", a, type(a))
print("b =", b, type(b))
print("c =", c, type(c))
print("d =", d, type(d))
print("e =", e, type(e))

# Запрос данных у пользователя
num_input = input("Введите число: ")
str_input = input("Введите строку: ")

# Сохраняем в переменные и выводим
print("Введенное число:", num_input)
print("Введенная строка:", str_input)

# Исследуем id изменяемых/неизменяемых объектов
print("\nИсследование изменяемости объектов:")
print(f"{'Объект':<10} | {'Исходный id':>20} | {'id после изменения':>20}")
print("-" * 55)

# Целое число (неизменяемый)
x = 10
print(f"{'int':<10} | {id(x):>20} |", end=" ")
x += 1
print(f"{id(x):>20}")

# Список (изменяемый)
y = [1, 2]
print(f"{'list':<10} | {id(y):>20} |", end=" ")
y.append(3)
print(f"{id(y):>20}")